package aulapoo;

public class Curso {
	private Long id;
	private String nome;
	private String cod;
	private String nomeCoord;
	private Long qtdAluno;
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getCod() {
		return cod;
	}
	public void setCod(String cod) {
		this.cod = cod;
	}
	public String getNomeCoord() {
		return nomeCoord;
	}
	public void setNomeCoord(String nomeCoord) {
		this.nomeCoord = nomeCoord;
	}
	public Long getQtdAluno() {
		return qtdAluno;
	}
	public void setQtdAluno(Long qtdAluno) {
		this.qtdAluno = qtdAluno;
	}
	
	

}
